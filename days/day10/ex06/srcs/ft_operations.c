int		ft_add(int a, int b)
{
	return (a + b);
}

int		ft_minus(int a, int b)
{
	return (a - b);
}

int		ft_mult(int a, int b)
{
	return (a * b);
}

int		ft_divided(int a, int b)
{
	return (a / b);
}

int		ft_modulo(int a, int b)
{
	return (a % b);
}
