#include "ft_stock_par.h"

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i = i + 1;
	}
}

void	ft_put_positive_nbr(int nb)
{
	if (nb <= 9)
		ft_putchar(nb + '0');
	else
	{
		ft_put_positive_nbr(nb / 10);
		ft_put_positive_nbr(nb % 10);
	}
}

void	ft_show_tab(struct s_stock_par *par)
{
	int		i;
	int		j;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].copy);
		ft_putchar('\n');
		ft_put_positive_nbr(par[i].size_param);
		ft_putchar('\n');
		j = 0;
		while (par[i].tab[j])
		{
			ft_putstr(par[i].tab[j]);
			ft_putchar('\n');
			j = j + 1;
		}
		i = i + 1;
	}
}
