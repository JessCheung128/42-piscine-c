#include "ft_header.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (str[i])
		ft_putchar(str[i++]);
}

void	ft_putstr_error(char *str)
{
	int		i;

	i = 0;
	while (str[i])
		write(2, &str[i++], 1);
}
