int		ft_button(int i, int j, int k)
{
	int		arr[3];

	arr[0] = i;
	arr[1] = j;
	arr[2] = k;
	i = 0;
	j = 1;
	while (i < 2)
	{
		while (j < 3)
		{
			if (arr[i] > arr[j])
			{
				k = arr[i];
				arr[i] = arr[j];
				arr[j] = k;
			}
			j = j + 1;
		}
		i = i + 1;
		j = i + 1;
	}
	return (arr[1]);
}
