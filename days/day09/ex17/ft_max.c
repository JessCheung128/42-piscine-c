int		ft_max(int *tab, int length)
{
	int		i;
	int		max;

	max = 0;
	i = 1;
	while (i < length)
	{
		max = tab[i] > tab[max] ? i : max;
		i = i + 1;
	}
	return (tab[max]);
}
