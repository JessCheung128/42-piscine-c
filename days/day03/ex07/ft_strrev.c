char	*ft_strrev(char *str)
{
	int		i;
	int		i2;
	char	tmp;

	i = 0;
	i2 = 0;
	while (str[i] != '\0')
		i++;
	i--;
	while (i >= i2)
	{
		tmp = str[i];
		str[i] = str[i2];
		i--;
		str[i2] = tmp;
		i2++;
	}
	return (str);
}
